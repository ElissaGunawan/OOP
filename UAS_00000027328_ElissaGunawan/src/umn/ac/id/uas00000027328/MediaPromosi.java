package umn.ac.id.uas00000027328;

public class MediaPromosi {
	public String nama;
	public int harga;
	
	public MediaPromosi() {}
	public MediaPromosi(String nama, int harga) {
		this.nama = nama;
		this.harga = harga;
	}
	public String getNama() {
		return nama;
	}
	public int getHarga() {
		return harga;
	}
}
